# โค้ดเวอร์ชันใหม่ของฉัน
# พัฒนาจากต้นแบบ โดย: [ใส่ชื่อ-นามสกุลของหนู]

print("=== โปรแกรมคำนวณพื้นที่ห้องและงบประมาณปูกระเบื้อง ===")

# 1. รับค่าความกว้างและความยาวจากผู้ใช้งาน
width = float(input("กรอกความกว้าง (เมตร): "))
length = float(input("กรอกความยาว (เมตร): "))

# 2. ตรวจสอบเงื่อนไข (ป้องกันการกรอกเลขติดลบ)
if width <= 0 or length <= 0:
    print("❌ ข้อผิดพลาด: ค่าความกว้างและความยาวต้องมากกว่า 0!")
else:
    # 3. คำนวณพื้นที่
    area = width * length
    
    # 4. ต่อยอด: เพิ่มตัวเลือกคำนวณงบประมาณ (ราคากระเบื้องเฉลี่ย ตารางเมตรละ 250 บาท)
    price_per_sqm = 250
    total_price = area * price_per_sqm
    
    # 5. แสดงผลลัพธ์อย่างสวยงาม
    print("-" * 40)
    print(f"📐 พื้นที่ทั้งหมด: {area:,.2f} ตารางเมตร")
    print(f"💰 ประเมินค่ากระเบื้อง (ตร.ม.ละ {price_per_sqm} บาท): {total_price:,.2f} บาท")
    print("-" * 40)
    print("✨ ขอบคุณที่ใช้บริการโปรแกรมของเรา ✨")
